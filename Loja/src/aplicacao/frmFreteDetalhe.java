    private void btnOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOKActionPerformed
        float numero = 0;
        if (txtTipo.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Informe o Tipo do Frete");
            return;
        }
        if (txtValor.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Informe o Valor do Frete");
            return;
        } else {
            try {
                String texto = txtValor.getText().replace(",", ".");
                numero = Float.parseFloat(texto);
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(this, "Digite um Valor válido!");
            }
        }
        try {
            Frete frete = new Frete();
            frete.setTipo(txtTipo.getText());
            frete.setValor(numero);
            int linha = freteDAO.inserir(frete);
            if (linha > 0) {
                JOptionPane.showMessageDialog(this, "Frete inserido com sucesso!");
                txtTipo.setText("");
                txtValor.setText("");
                txtTipo.requestFocus();
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Erro ao Inserir!");
        }
        
        
    }//GEN-LAST:event_btnOKActionPerformed

    private void btnCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelarActionPerformed
        Object[] opcao = {"Não", "Sim"};
        int opcaoSelecionada = JOptionPane.showOptionDialog(this, "Deseja realmente sair?", "Aviso",
                JOptionPane.DEFAULT_OPTION, JOptionPane.WARNING_MESSAGE, null, opcao, opcao[0]);
        if (opcaoSelecionada == 1) {
            this.dispose();
        }
    }//GEN-LAST:event_btnCancelarActionPerformed

