    private void txtValorKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtValorKeyTyped
        boolean ok = false;
        char c = evt.getKeyChar();
        if ((c == KeyEvent.VK_0)
                || (c == KeyEvent.VK_1)
                || (c == KeyEvent.VK_2)
                || (c == KeyEvent.VK_3)
                || (c == KeyEvent.VK_4)
                || (c == KeyEvent.VK_5)
                || (c == KeyEvent.VK_6)
                || (c == KeyEvent.VK_7)
                || (c == KeyEvent.VK_8)
                || (c == KeyEvent.VK_9)
                || (c == KeyEvent.VK_COMMA)
                || (c == KeyEvent.VK_BACK_SPACE)
                || (c == KeyEvent.VK_DELETE)) {
            ok = true;
        }
        if (!ok) {
            getToolkit().beep();
            evt.consume();
        }
    }//GEN-LAST:event_txtValorKeyTyped

