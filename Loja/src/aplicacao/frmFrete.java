    private void preencherTabela() {
        modelo.getDataVector().clear();
        try {
            for (Frete frete : freteDAO.listar()) {
                modelo.addRow(new Object[]{frete.getCodigo(),
                    frete.getTipo(),
                    frete.getValor()});
            }
        } catch (Exception e) {
            throw e;
        }
}

